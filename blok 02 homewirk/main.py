# импортируем модуль math для вычисления площади круга
import math

# запрашиваем тип фигуры
figure = input("Введите тип фигуры: ")

# в зависимости от типа фигуры запрашиваем характеристики и вычисляем площадь
if figure == "Круг":
  # запрашиваем радиус круга
  radius = float(input("Введите радиус круга: "))
  # вычисляем площадь круга по формуле S = pi * r^2
  area = math.pi * radius ** 2
  # выводим результат
  print(f"Площадь круга: {round(area, 2)}")
elif figure == "Треугольник":
  # запрашиваем длины сторон треугольника
  a = float(input("Введите длину стороны a: "))
  b = float(input("Введите длину стороны b: "))
  c = float(input("Введите длину стороны c: "))
  # вычисляем полупериметр треугольника по формуле p = (a + b + c) / 2
  p = (a + b + c) / 2
  # вычисляем площадь треугольника по формуле Герона S = sqrt(p * (p - a) * (p - b) * (p - c))
  area = math.sqrt(p * (p - a) * (p - b) * (p - c))
  # выводим результат
  print(f"Площадь треугольника: {round(area, 2)}")
elif figure == "Прямоугольник":
  # запрашиваем длины сторон прямоугольника
  a = float(input("Введите длину стороны a: "))
  b = float(input("Введите длину стороны b: "))
  # вычисляем площадь прямоугольника по формуле S = a * b
  area = a * b
  # выводим результат
  print(f"Площадь прямоугольника: {round(area, 2)}")
else:
  # если тип фигуры неизвестен, выводим сообщение об ошибке
  print("Неверный тип фигуры")

  
  
  